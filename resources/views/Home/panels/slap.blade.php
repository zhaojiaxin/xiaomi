            //工具类
            var MiBuyPhoneUtil = {
                tmpl: function(name, obj) {
                    var tmpl = document.getElementById(name);
                    var tmplHtml = tmpl.innerHTML;
                    var doTtmpl = doT.template(tmplHtml);
                    return doTtmpl(obj);
                }
            };

            //View 层
            var MiBuyPhoneView = {
                addContent: function(data) {
                    var name = 'content_tmpl';
                    var obj = data;
                    var str = MiBuyPhoneUtil.tmpl(name, obj);
                    return str;
                }
            };

            //Model 层
            var MiBuyPhoneModel = {
                config: function() {
                    var _this = this;

                    //原始数据
                    _this.PAGEDATA = window.phoneList;

                    //层级数据
                    _this.GRADEDATA = {};

                    //打破初始化模糊层
                    _this.INITFILTER = false;

                    //层级数组
                    _this.LEVELARR = [];

                    //记录HASH串的数组
                    _this.HASHARR = [];
                },
                pageInitData: function() {
                    var _this = this;
                    var data = _this.PAGEDATA;
                    var obj = {
                        parent: data,
                        arr: data.children
                    };
                    var key = data.fontTitle_level;
                    _this.GRADEDATA[key] = obj;
                    return obj;
                }
            };

            //Controller层
            var MiBuyPhoneController = {
                config: function() {
                    MiBuyPhoneModel.config();
                },
                init: function() {
                    var _this = this;
                    _this.config();
                    _this.runAll();
                },
                runAll: function() {
                    var _this = this;

                    //首先初始化页面
                    _this.pageInitLoad();

                    //点击事件的触发
                    _this.clickAvailable();

                    //根据Hash值，触发相应的
                    _this.triggerClickByHash();
                },

                //首先初始化页面
                pageInitLoad: function() {
                    var data = MiBuyPhoneModel.pageInitData();
                    var str = MiBuyPhoneView.addContent(data);

                    //加载第一层数据
                    $('.J_choose_block').append(str);

                    //判断是否有第二层
                    if (data.arr.length > 0) {
                        var children = data.arr[0].children;
                        if (children) {
                            var seObj = {
                                parent: data.arr[0],
                                arr: children
                            };
                            var key = data.arr[0].fontTitle_level;
                            MiBuyPhoneModel.GRADEDATA[key] = seObj;
                            var secondStr = MiBuyPhoneView.addContent(seObj);

                            $('.J_choose_block').append(secondStr);
                            $('.J_choose_block').find('.step-unit').eq(1).addClass('vague');
                        }
                    }
                },

                //点击事件的触发
                clickAvailable: function() {
                    var _this = this;
                    $('.J_choose_block').on('click', '.version',
                    function() {

                        //判断是是否缺货
                        var stockFlag = $(this).hasClass('stockout');
                        if (stockFlag) {
                            return false;
                        }

                        //判断是否元素被盖住了
                        if ($(this).parent().parent().parent().hasClass('vague')) {
                            return false;
                        }

                        //否则把遮罩层，删掉
                        $('.J_choose_block').find('.step-unit').eq(1).removeClass('vague');
                        var level = $(this).attr('data-level');
                        if ($.inArray(level, MiBuyPhoneModel.LEVELARR) === -1) {
                            MiBuyPhoneModel.LEVELARR.push(level);
                        }

                        //得到被点击的这一层级的数据
                        var obj = MiBuyPhoneModel.GRADEDATA[level];
                        var index = $(this).attr('data-index');

                        //添加active状态
                        $(this).parent().siblings('li').find('.version').removeClass('active');
                        $(this).addClass('active');
                        var tipInfo = $(this).attr('data-tip');

                        //如果有提示信息的话，显示提示信息
                        if (tipInfo) {
                            $('.J_tipInfo').html('下一步：' + tipInfo);
                        }

                        var levelIndex = $.inArray(level, MiBuyPhoneModel.LEVELARR) + 1;

                        //赋值hash串
                        MiBuyPhoneModel.HASHARR[levelIndex - 1] = obj.arr[index].phone_type_tag;
                        var nullIndex = MiBuyPhoneModel.HASHARR.length;
                        if (levelIndex < nullIndex) {
                            MiBuyPhoneModel.HASHARR.splice(levelIndex, nullIndex - levelIndex);
                        }
                        nullIndex = MiBuyPhoneModel.HASHARR.length;

                        //判断有没有空格
                        var isNull = $.inArray('', MiBuyPhoneModel.HASHARR);
                        if (isNull !== -1) {
                            nullIndex = isNull;
                        }
                        var realHashArr = [];
                        for (var i = 0; i < nullIndex; i++) {
                            realHashArr.push(MiBuyPhoneModel.HASHARR[i]);
                        }

                        //首先把底层的全部清掉
                        $('.J_choose_block').find('.step-unit').each(function(i) {
                            if (i >= levelIndex) {
                                $(this).remove();
                            }
                        });

                        _this.CUROBJ = obj.arr[index];

                        var children = obj.arr[index].children;

                        //表示到了最下面的一级
                        if (!children) {
                            _this.activeBtn(_this.CUROBJ);
                            return false;
                        }

                        _this.removeBtnActive();

                        if (children) {
                            var newObj = {
                                parent: obj.arr[index],
                                arr: children
                            };

                            var key = obj.arr[index].fontTitle_level;

                            MiBuyPhoneModel.GRADEDATA[key] = newObj;

                            var secondStr = MiBuyPhoneView.addContent(newObj);

                            if ($('.J_choose_block').find('.step-unit').eq(level).length !== 0) {

                                $('.J_choose_block').find('.step-unit').eq(level).replaceWith(secondStr);

                                $('.J_choose_block').find('.step-unit').each(function() {
                                    if (i > levelIndex) {
                                        $(this).remove();
                                    }
                                });
                            } else {
                                $('.J_choose_block').append(secondStr);
                            }
                        }
                    });

                    //给下一步按钮添加点击事件
                    //XIAOMI.app.addShopCartEvent
                    MI.bigtapAddCart({
                        obj: '.J_btn_next',
                        before: function() {
                            var curObj = _this.CUROBJ;
                            var gid = $('.J_btn_next').attr('data-gid');

                            // isAddCart 来控制是否是 预售跳转
                            if (!gid || typeof curObj.isAddCart !== 'undefined' && (curObj.isAddCart === 'false' || !curObj.isAddCart)) {
                                location.href = curObj.hrefUrl;
                                return false;
                            }
                        },
                        callback: function(data) {
                            if (data.code === 1) {
                                var gid = $('.J_btn_next').attr('data-gid');
                                location.href = MI.GLOBAL_CONFIG.cartSite + '/cart/recommend/goods_id/' + gid;
                            } else {
                                alert(data.msg);
                            }
                        }
                    });
                },
                triggerClickByHash: function() {
                    var mainObj = window.phoneList;
                    var index = 0;
                    var hash = location.hash;
                    var phoneTag = [];
                    var callback = function() {
                        var children = mainObj.children;
                        var tag = phoneTag[index];
                        var currentIndex = 0;
                        for (var i = 0; i < children.length; i++) {
                            var currentObj = children[i];
                            if (tag === currentObj.phone_type_tag) {
                                currentIndex = i;
                                $('.J_choose_block').find('.step-unit').eq(index).find('.version').eq(i).trigger('click');
                            }
                        }
                        index++;
                        if (index < phoneTag.length) {
                            mainObj = children[currentIndex];
                            setTimeout(callback, 100);
                        }
                    };

                    if (hash !== '') {
                        hash = hash.substring(1);
                        phoneTag = hash.split('-');
                        setTimeout(callback, 100);
                    }
                },

                //把按钮变灰，去除链接
                removeBtnActive: function() {
                    $('.J_btn_next').removeAttr('href').removeClass('btn-primary').removeAttr('data-gid').attr('data-disabled', 'true').addClass('btn-dake').removeAttr('onclick');
                },

                //把按钮激活
                activeBtn: function(obj) {
                    $('.J_btn_next').removeAttr('data-disabled').removeClass('btn-dake').addClass('btn-primary');

                    //如果有加入购物车的标志, isAddCart 来控制是否是 预售跳转
                    if (obj.isAddCart || typeof obj.isAddCart === 'undefined') {
                        var cartId = obj.goods_id;
                        $('.J_btn_next').attr('data-gid', cartId);
                    }
                }
